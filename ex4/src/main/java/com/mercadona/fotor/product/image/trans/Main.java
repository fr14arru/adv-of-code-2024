    private static char[][] getCharsMatrix(String input) {

        String[] split = input.split("\n");

        char[][] allChars = new char[split.length][split[0].length()];

        for (int i = 0; i < split.length; i++) {

            String splitPart = split[i];

            for (int j = 0; j < splitPart.length(); j++) {

                allChars[i][j] = splitPart.charAt(j);
            }
        }

        return allChars;
    }

    public static int countXMAS(char[][] grid, String word) {

        int result = 0;

        int rows = grid.length;
        int cols = grid[0].length;

        int[][] directions = {
            {0, 1},   // Horizontal derecha
            {0, -1},  // Horizontal izquierda
            {1, 0},   // Vertical abajo
            {-1, 0},  // Vertical arriba
            {1, 1},   // Diagonal principal abajo-derecha
            {-1, -1}, // Diagonal principal arriba-izquierda
            {1, -1},  // Diagonal secundaria abajo-izquierda
            {-1, 1},  // Diagonal secundaria arriba-derecha
        };

        // Recorrer cada celda de la matriz
        for (int x = 0; x < rows; x++) {
            for (int y = 0; y < cols; y++) {

                // Intentar buscar en cada direcciÃ³n
                for (int[] dir : directions) {

                    if (search(grid, word, x, y, dir[0], dir[1])) {

                        result++;
                    }
                }
            }
        }

        return result;
    }

    private static boolean search(char[][] grid, String word, int x, int y, int dx, int dy) {
        int rows = grid.length;
        int cols = grid[0].length;

        for (int i = 0; i < word.length(); i++) {
            int nx = x + i * dx;
            int ny = y + i * dy;

            if (nx < 0 || nx >= rows || ny < 0 || ny >= cols || grid[nx][ny] != word.charAt(i)) {
                return false;
            }
        }

        return true;
    }

