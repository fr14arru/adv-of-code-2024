    public static int countCrossedWords(char[][] grid) {

        int result = 0;

        int rows = grid.length;
        int cols = grid[0].length;

        for (int x = 0; x < rows; x++) {
            for (int y = 0; y < cols; y++) {

                if (grid[x][y] == 'A') {

                    try {

                        String wordDiag1 = new String(new char[]{grid[x - 1][y - 1], grid[x][y], grid[x + 1][y + 1]});
                        String wordDiag2 = new String(new char[]{grid[x - 1][y + 1], grid[x][y], grid[x + 1][y - 1]});

                        if (wordDiag1.equals(MAGIC_WORD_2) && wordDiag2.equals(MAGIC_WORD_2_REVERSED) ||
                            wordDiag1.equals(MAGIC_WORD_2_REVERSED) && wordDiag2.equals(MAGIC_WORD_2) ||
                            wordDiag1.equals(MAGIC_WORD_2) && wordDiag2.equals(MAGIC_WORD_2) ||
                            wordDiag2.equals(MAGIC_WORD_2_REVERSED) && wordDiag1.equals(MAGIC_WORD_2_REVERSED)) {

                            result++;

                        }
                    } catch (ArrayIndexOutOfBoundsException ignored) {
                    }
                }
            }
        }

        return result;
    }

